    private void btn_equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_equalActionPerformed
            valor2 = Double.parseDouble(visor_Decimal.getText());
            
            if(sinal == "somar"){
                resultado = valor1+valor2;
                visor_Decimal.setText(String.valueOf(resultado));
                visor_Binary.setText(String.valueOf(Integer.toBinaryString(resultado.intValue())));
                visor_Hexa.setText(String.valueOf(Integer.toHexString(resultado.intValue())));  
                visor_Octal.setText(String.valueOf(Integer.toOctalString(resultado.intValue())));        
            }
            if(sinal == "diminuir"){
                resultado = valor1-valor2;
                visor_Decimal.setText(String.valueOf(resultado));
                visor_Binary.setText(String.valueOf(Integer.toBinaryString(resultado.intValue())));
                visor_Hexa.setText(String.valueOf(Integer.toHexString(resultado.intValue())));  
                visor_Octal.setText(String.valueOf(Integer.toOctalString(resultado.intValue())));        
            }
            if(sinal == "multiplicar"){
                resultado = valor1*valor2;
                visor_Decimal.setText(String.valueOf(resultado));
                visor_Binary.setText(String.valueOf(Integer.toBinaryString(resultado.intValue())));
                visor_Hexa.setText(String.valueOf(Integer.toHexString(resultado.intValue())));  
                visor_Octal.setText(String.valueOf(Integer.toOctalString(resultado.intValue())));        
            }
            if(sinal == "dividir"){
                resultado = valor1/valor2;
                visor_Decimal.setText(String.valueOf(resultado));
                visor_Binary.setText(String.valueOf(Integer.toBinaryString(resultado.intValue())));
                visor_Hexa.setText(String.valueOf(Integer.toHexString(resultado.intValue())));  
                visor_Octal.setText(String.valueOf(Integer.toOctalString(resultado.intValue())));        
            }
            if(valor2 == null){
                visor_Decimal.setText(String.valueOf(valor1));
                visor_Binary.setText(String.valueOf(Integer.toBinaryString(valor1.intValue())));
                visor_Hexa.setText(String.valueOf(Integer.toHexString(valor1.intValue())));  
                visor_Octal.setText(String.valueOf(Integer.toOctalString(valor1.intValue())));        
            }

            
    }//GEN-LAST:event_btn_equalActionPerformed

